    private void filterHocVien(String query) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(tableHV);
        tbHocVien.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }

