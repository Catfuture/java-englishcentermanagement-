    private void tbKhoaHocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKhoaHocMouseClicked
        // TODO add your handling code here:
        int row = tbKhoaHoc.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn lớp học!");
        }else{
            int si_so = (int) tbKhoaHoc.getValueAt(row, 5);
            if(si_so == 0){
                btnXoaKH.setEnabled(true);
                btnSuaKH.setEnabled(true);
            }else{
                btnXoaKH.setEnabled(false);
                btnSuaKH.setEnabled(false);
            }
        }
    }//GEN-LAST:event_tbKhoaHocMouseClicked
    private void HienThiHocVien() {
        tableHV = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tbHocVien.setModel(tableHV);
        tableHV.addColumn("Mã học viên");
        tableHV.addColumn("Tên học viên");
        tableHV.addColumn("Ngày sinh");
        tableHV.addColumn("Phái");
        tableHV.addColumn("Địa chỉ");
        tableHV.addColumn("Liên hệ");
        tableHV.addColumn("Mã khóa học");

        ArrayList<HocVien> dataHV = qlhv.DocDL();
        for (HocVien hv : dataHV) {
            tableHV.addRow(new Object[]{hv.getMa_hv(), hv.getTen_hv(), hv.getNgay_sinh(), hv.getPhai(), hv.getDia_chi(), hv.getLien_he(), hv.getMa_khoa_hoc()});
        }
    }

