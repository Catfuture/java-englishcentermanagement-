    private void btnXOALHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXOALHActionPerformed
        // TODO add your handling code here:
        int row = tbHocVien.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn học viên!");
        } else {
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa?!");

            if (confirm == JOptionPane.YES_OPTION) {
                String hvID = String.valueOf(tbHocVien.getValueAt(row, 0));
                if (qlhv.XoaDL(hvID) == 0) {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    HienThiHocVien();
                }
            }
        }
    }//GEN-LAST:event_btnXOALHActionPerformed

